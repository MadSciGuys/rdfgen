#ifndef COLOR_H
#define COLOR_H

// Define shortcuts for ASCII color control codes:
#define RED "\x1b[31m"
#define GREEN "\x1b[32m"
#define YELLOW "\x1b[33m"
#define BLUE "\x1b[34m"
#define MAGENTA "\x1b[35m"
#define CYAN "\x1b[36m"
#define BOLD "\x1b[1m"
#define UNDERLINE "\x1b[4m"
#define REVERSE "\x1b[7m"
#define BLINK "\x1b[5m"
#define BLINK_EXPERIMENTAL "\x1b[6m"
#define RESET "\x1b[0m"

#endif
